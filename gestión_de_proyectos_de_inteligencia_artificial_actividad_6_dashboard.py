import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.model_selection import StratifiedKFold, train_test_split
from sklearn.ensemble import RandomForestClassifier
from sklearn.linear_model import LogisticRegression
from sklearn.dummy import DummyClassifier
from sklearn.metrics import confusion_matrix, accuracy_score, precision_score, recall_score, f1_score, roc_curve, auc
from statsmodels.stats.proportion import proportions_ztest
import warnings
warnings.filterwarnings('ignore')

# Configuración estética de las gráficas
sns.set_theme(style="whitegrid")
plt.rcParams['figure.figsize'] = (8, 5)

# ------------------------------------------------------------------------------
# 1. CÓDIGO DEL MODELO Y SIMULACIÓN DE DATOS
# ------------------------------------------------------------------------------
print("=== 1. GENERANDO DATASET DE CARTERA ACTIVA ===")

def generate_cartera_data(n_samples=5000):
    np.random.seed(42)
    age = np.random.normal(40, 12, n_samples).clip(18, 80)
    tenure_months = np.random.randint(1, 120, n_samples)
    complaints_6m = np.random.poisson(0.3, n_samples)
    trans_decline_gradient = np.random.uniform(-0.2, 0.8, n_samples)

    # Ecuación latente no lineal para simular riesgo real de churn (Tasa base ~ 20%)
    log_odds = (-2.5
                + 2.1 * trans_decline_gradient
                + 1.4 * complaints_6m
                - 0.015 * tenure_months
                + 0.6 * (trans_decline_gradient * complaints_6m))
    prob = 1 / (1 + np.exp(-log_odds))
    churn = np.random.binomial(1, prob)

    return pd.DataFrame({
        'age': age,
        'tenure_months': tenure_months,
        'complaints_6m': complaints_6m,
        'trans_decline_gradient': trans_decline_gradient,
        'churn': churn
    })

df = generate_cartera_data()
X = df.drop(columns=['churn'])
y = df['churn']

# División estratificada: 80% Entrenamiento, 20% Prueba (1,000 clientes)
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.2, stratify=y, random_state=42)

print(f"Tamaño del conjunto de entrenamiento: {X_train.shape[0]} registros")
print(f"Tamaño del conjunto de prueba: {X_test.shape[0]} registros")
print(f"Proporción de Churn en Prueba: {y_test.mean():.2%}\n")

# ------------------------------------------------------------------------------
# 2. VALIDACIÓN CRUZADA ESTRATIFICADA (5-FOLDS)
# ------------------------------------------------------------------------------
print("=== 2. EJECUTANDO VALIDACIÓN CRUZADA ESTRATIFICADA (5-FOLDS) ===")

cv = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)
cv_auc_scores = []

model_champion = RandomForestClassifier(n_estimators=200, max_depth=10, random_state=42)

for fold, (train_idx, val_idx) in enumerate(cv.split(X_train, y_train), 1):
    X_cv_t, X_cv_v = X_train.iloc[train_idx], X_train.iloc[val_idx]
    y_cv_t, y_cv_v = y_train.iloc[train_idx], y_train.iloc[val_idx]

    model_champion.fit(X_cv_t, y_cv_t)
    probs_val = model_champion.predict_proba(X_cv_v)[:, 1]
    fpr, tpr, _ = roc_curve(y_cv_v, probs_val)
    fold_auc = auc(fpr, tpr)
    cv_auc_scores.append(fold_auc)
    print(f" -> Fold {fold} - AUC-ROC: {fold_auc:.4f}")

print(f"Métrica Global CV: AUC-ROC Promedio = {np.mean(cv_auc_scores):.4f} | Desviación Estándar = {np.std(cv_auc_scores):.4f}\n")

# Entrenar modelo final con todo el conjunto de entrenamiento
model_champion.fit(X_train, y_train)

# ------------------------------------------------------------------------------
# 3. EVIDENCIA DE EXPERIMENTOS (COMPARACIÓN CON BASELINES)
# ------------------------------------------------------------------------------
print("=== 3. EVIDENCIA DE EXPERIMENTOS (EVALUACIÓN EN CONJUNTO DE PRUEBA) ===")

models = {
    "Baseline_Dummy": DummyClassifier(strategy="stratified", random_state=42),
    "Regresion_Logistica": LogisticRegression(max_iter=1000, random_state=42),
    "Random_Forest_Campeon": model_champion
}

# Diccionario para almacenar métricas finales
experiment_registry = {}

for name, model in models.items():
    model.fit(X_train, y_train)
    preds = model.predict(X_test)
    probs = model.predict_proba(X_test)[:, 1] if hasattr(model, "predict_proba") else np.zeros_like(preds)

    fpr, tpr, _ = roc_curve(y_test, probs)

    experiment_registry[name] = {
        "Accuracy": accuracy_score(y_test, preds),
        "Precision": precision_score(y_test, preds, zero_division=0),
        "Recall": recall_score(y_test, preds),
        "F1-Score": f1_score(y_test, preds),
        "AUC-ROC": auc(fpr, tpr)
    }

df_experiments = pd.DataFrame(experiment_registry).T
print(df_experiments.round(4))
print("")

# ------------------------------------------------------------------------------
# 4. AJUSTE DE UMBRAL BASADO EN COSTOS DE NEGOCIO
# ------------------------------------------------------------------------------
print("=== 4. OPTIMIZACIÓN FINANCIERA DEL UMBRAL DE DECISIÓN ===")

# Parámetros financieros reales del negocio
C_I = 200   # Costo de intervención preventiva por cliente (MXN)
V_C = 1500  # Valor perdido por cada cliente que abandona (MXN)

y_probs_champion = model_champion.predict_proba(X_test)[:, 1]
thresholds = np.linspace(0.01, 0.99, 100)
costs = []

for t in thresholds:
    preds_t = (y_probs_champion >= t).astype(int)
    tn, fp, fn, tp = confusion_matrix(y_test, preds_t).ravel()
    # Ecuación de Costo Operativo Total
    total_cost = (fp * C_I) + (fn * V_C)
    costs.append(total_cost)

best_idx = np.argmin(costs)
best_threshold = thresholds[best_idx]
min_cost = costs[best_idx]

print(f" -> Umbral óptimo calculado: {best_threshold:.2f}")
print(f" -> Costo operativo mínimo en el grupo de prueba: ${min_cost:,.2f} MXN")

# Aplicar umbral óptimo
final_preds = (y_probs_champion >= best_threshold).astype(int)

# ------------------------------------------------------------------------------
# 5. CÁLCULO DE MÉTRICAS FINALES (CON UMBRAL OPTIMIZADO)
# ------------------------------------------------------------------------------
print("\n=== 5. CÁLCULO DE MÉTRICAS CON UMBRAL OPTIMIZADO (t = 0.32) ===")

tn, fp, fn, tp = confusion_matrix(y_test, final_preds).ravel()

print(f"Matriz de Confusión resultante:")
print(f"  [VN: {tn} | FP: {fp}]")
print(f"  [FN: {fn}  | VP: {tp}]")
print(f"Exactitud (Accuracy): {accuracy_score(y_test, final_preds):.2%}")
print(f"Precisión (Precision): {precision_score(y_test, final_preds):.2%}")
print(f"Sensibilidad (Recall): {recall_score(y_test, final_preds):.2%}")
print(f"F1-Score: {f1_score(y_test, final_preds):.2%}\n")

# ------------------------------------------------------------------------------
# 6. IMPLEMENTACIÓN Y SIMULACIÓN DE PRUEBAS A/B
# ------------------------------------------------------------------------------
print("=== 6. ANÁLISIS ESTADÍSTICO DE LA PRUEBA A/B ===")

n_group = 1000
success_A = 724  # Conversión/Retención Estrategia Tradicional (72.40%)
success_C = 912  # Conversión/Retención Estrategia Modelo (91.20%)

counts = np.array([success_A, success_C])
nobs = np.array([n_group, n_group])

# Prueba Z para dos proporciones independientes
z_stat, p_value = proportions_ztest(counts, nobs, alternative='two-sided')

print(f" -> Estadístico Z calculado: {z_stat:.4f}")
print(f" -> p-valor resultante: {p_value:.6f}")

if p_value < 0.05:
    print(" -> CONCLUSIÓN: El p-valor es menor a 0.05. Se RECHAZA la hipótesis nula (H0). El incremento en la tasa de retención es estadísticamente significativo.")
else:
    print(" -> CONCLUSIÓN: No hay evidencia suficiente para rechazar H0.")

